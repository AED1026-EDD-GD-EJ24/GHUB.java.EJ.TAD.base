package miPrincipal;

public class Calculadora {

    public int suma(int x, int y) {
        return x + y;
    }
    
    public int resta(int x, int y) {
        return x-y;
    }

    public int multiplica(int x, int y) {
        return x * y;
    }

    public int divide(int x, int y) {
        return x / y;
    }
}